class Relational // Relational operator also known as Comparision operator and give output either true or false
{
    public static void main (String arg[])
    {
        int a=5;
        int b = 7;
        System.out.println("a == b = "  + (a==b));
        System.out.println("a != b = "  + (a!=b));
        System.out.println("a <= b = "  + (a<=b));
        System.out.println("a >= b = "  + (a>=b));
        System.out.println("a < b = "  + (a<b));
        System.out.println("a > b = "  + (a>b));
    }
}